#include "libft.h"

char	*ft_strchr(char *str, int c)
{
	while (*str != '\0')
	{
		if (*str == (char)c)
		{
			return (str);
		}
		str++;
	}
	if (*str == c)
	{
		return (str);
	}
	return (NULL);
}
