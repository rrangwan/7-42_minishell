#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	int				i;
	unsigned char	*str;

	str = b;
	i = 0;
	while (n != 0)
	{
		str[i] = c;
		i++;
		n--;
	}
	return (b);
}
