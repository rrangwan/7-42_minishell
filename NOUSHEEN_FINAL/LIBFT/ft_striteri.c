#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		l;
	int		i;

	i = 0;
	if (!s || !f)
		return ;
	l = ft_strlen(s);
	while (i < l)
	{
		(*f)(i, &s[i]);
		i++;
	}
}
